<script>
    const app = new Vue({
        el: '#app',
        data() {
            return {
                activeName: "bestwehotel",
                loading: false,
                submitLoading: false,
                ruleForm: {
                    bestwehotel: {
                        app_id: '',
                        key: ''
                    }
                },
                rules: {

                }
            };
        },
        created() {
            this.getSetting()
        },
        methods: {
            getSetting(){
                this.loading = true;
                request({
                    params: {
                        r: 'plugin/hotel/mall/platforms/setting'
                    },
                }).then(e => {
                    this.loading = false;
                    if (e.data.code === 0) {
                        var setting = typeof e.data.data.setting == "object" ? e.data.data.setting : {};
                        if(typeof setting['bestwehotel'] == "undefined"){
                            setting['bestwehotel'] = {app_id: '', key: ''};
                        }
                        this.ruleForm = setting;

                    } else {
                        this.$message.error(e.data.msg);
                    }
                })
            },
            submitForm(formName){
                this.$refs[formName].validate(valid => {
                    if (valid) {
                        this.submitLoading = true;
                        request({
                            params: {
                                r: 'plugin/hotel/mall/platforms/setting',
                            },
                            method: 'post',
                            data: {
                                jsonSettingArray: JSON.stringify(this.ruleForm)
                            },
                        }).then(e => {
                            this.submitLoading = false;
                            if (e.data.code === 0) {
                                this.$message.success(e.data.msg);
                            } else {
                                this.$message.error(e.data.msg);
                            }
                        }).catch(e => {
                        });
                    } else {
                        this.$message.error('部分参数验证不通过');
                    }
                });
            }
        },
        computed: {}
    });
</script>
