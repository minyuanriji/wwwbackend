namespace app\plugins\agent\forms\mall;


use app\core\ApiCode;
use app\plugins\agent\models\Agent;
use app\models\BaseModel;


class AgentRemarksForm extends BaseModel
{

    public $id;
    public $remarks;


    public function rules()
    {
        return [
            [['id'], 'required'],
            [['id'], 'integer'],
            [['remarks'], 'string']


        ]; // TODO: Change the autogenerated stub
    }

    /**
     * @Author: 广东七件事 ganxiaohao
     * Date: 2020-05-10
     * Time: 21:39
     * @Note:保存备注
     */
    public function save()
    {
        if (!$this->validate()) {
            return $this->responseErrorInfo();
        }
        $agent = Agent::findOne(['id' => $this->id, 'is_delete' => 0]);
        if (!$agent) {
            return ['code' => ApiCode::CODE_FAIL, 'msg' => '该分销商不存在'];
        }

        $agent->remarks = $this->remarks;
        if (!$agent->save()) {
            return $this->responseErrorMsg($agent);
        }
        return  ['code'=>ApiCode::CODE_SUCCESS,'msg'=>'保存成功'];
    }



}